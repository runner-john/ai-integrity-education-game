    function shuffleArray(array) {
        const newArray = [...array];
        for (let i = newArray.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
        }
        return newArray;
    }

    function calculateCosineSimilarity(vecA, vecB) {
        let dotProduct = 0;
        let normA = 0;
        let normB = 0;
        
        const allKeys = new Set([...Object.keys(vecA), ...Object.keys(vecB)]);
        
        for (const key of allKeys) {
            const a = vecA[key] || 0;
            const b = vecB[key] || 0;
            dotProduct += a * b;
            normA += a * a;
            normB += b * b;
        }
        
        if (normA === 0 || normB === 0) return 0;
        return dotProduct / (Math.sqrt(normA) * Math.sqrt(normB));
    }

    function getTFIDFVector(text, documentFrequency, totalDocuments) {
        const words = text.toLowerCase().split(/\s+/);
        const wordCount = {};
        const vector = {};
        
        for (const word of words) {
            wordCount[word] = (wordCount[word] || 0) + 1;
        }
        
        const maxFreq = Math.max(...Object.values(wordCount), 1);
        
        for (const [word, count] of Object.entries(wordCount)) {
            const tf = 0.5 + 0.5 * (count / maxFreq);
            const df = documentFrequency[word] || 1;
            const idf = Math.log(totalDocuments / df);
            vector[word] = tf * idf;
        }
        
        return vector;
    }

